number_one = 10
number_two = 4

result = number_one + number_two
print ("Suma", result)

result = number_one - number_two
print ("Resta", result)

result = number_one * number_two # Regresa un número flotante  
print ("Multiplicación", result)

result = number_one // number_two # Regresa un número entero
print("División parte entera", result)

result = number_one ** number_two # Regresa el exponencial de un número
print("Exponencial", result)